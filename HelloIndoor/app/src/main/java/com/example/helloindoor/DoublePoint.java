package com.example.helloindoor;

import android.graphics.Point;

    public class DoublePoint {

        public DoublePoint(double x, double y) {
            this.x = x;
            this.y = y;
        }

        public double x;
        public double y;


        public double getX() {
            return this.x;
        }

        public void setX(double x) {
            this.x = x;
        }

        public double getY() {
            return this.y;
        }

        public void setY(double y) {
            this.y = y;
        }
    }


